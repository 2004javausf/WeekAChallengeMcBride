package com.revature.javabeans;

public class Name {

private String name;
private double height;
private int age;



	public void setName(String name)
	{
		this.name = name;
	}
	public String getName ()
	{
		return name;
	}
	
	public void setHeight(double height)
	{
		this.height = height;
	}
	public double getHeight ()
	{
		return height;
	}
	public void setAge(int age)
	{
		this.age = age;
	}
	public int getAge()
	{
		return age;
	}
}
